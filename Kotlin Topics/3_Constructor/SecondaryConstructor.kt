Example :-3
 
Notes :- 

1) Primary Constructor call is expected from the Secondary constructor using this keyword, If the user has written a Primary constructor.
2) Reason is by default a primary constructor will be called by compiler, If user has not written.
3) If the user has written a primary constructor then user needs to call primary constructor.
 

