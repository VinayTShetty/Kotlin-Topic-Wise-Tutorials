Example :-5
 
Notes :- 

We can use both val and var in companion Object.

Additional Information :- 
 
Programe :-

fun main() {
    Demo.callme()
    println(Demo.username)
    println(Demo.password)
}
class Demo{
    companion object {
        val username="Demo";
        var password="123456"
        fun callme(){
            println("You called Me")
        }
    }
}

 
Output :
 
You called Me
Demo
123456
 
