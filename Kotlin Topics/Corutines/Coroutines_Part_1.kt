Example :-9
 
Notes :- 
Using suspending function in finally block.
For that we need to use withContext and inside that write delay function. 

Additional Information :- 
 
Programe :-
 
import kotlinx.coroutines.*
fun main(args: Array<String>) = runBlocking {
    println("Main Program Starts : ${Thread.currentThread().name}")
    val job: Job = launch(Dispatchers.Default) {
        try {
            for (i in 0..500) {
                println("${i}")
                delay(5)
            }
        }catch (ex:CancellationException){
            println("\n Exception caught safely")
        }finally {
           withContext(NonCancellable){  // As a paramter pass non cancellable as a companion Object.
               delay(100)
               println(" Finally Blcok executed")
           }
        }
    }
    delay(700)
    job.cancelAndJoin()
    println("Main Program Ends : ${Thread.currentThread().name}")
}

 
