Topics Additional Information :- 
A class can also declare secondary constructors, which are prefixed with constructor
-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 
Example :-1
 
Notes :- 

Secondary constructor is used to initialize the class and introduce some extra logic. Kotlin may have one or more secondary constructors.

Additional Information :- 
 
Programe :-
 
fun main() {
    println("Main Func Starts")
    val test=Test("Vinay T Shetty")
}

class Test{
    constructor(username:String){
        println("String arg Constructor ${username}")
    }
}
 
Output :
 
Main Func Starts
String arg Constructor Vinay T Shetty
 
----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
 
Example :-2
 
Notes :- Overloading of the Constructor is Valid.
 
Additional Information :- 
 
Programe :-
fun main() {
    println("Main Func Starts")
    val test=Test("Vinay T Shetty")
    val test_1=Test(2509)
}

class Test{
    constructor(username:String){
        println("String arg Constructor ${username}")
    }
    constructor(registrationNumbner:Int){
        println("Int arg Constructor ${registrationNumbner}")
    }
}
 
Output :
 
Main Func Starts
String arg Constructor Vinay T Shetty
String arg Constructor 2509
