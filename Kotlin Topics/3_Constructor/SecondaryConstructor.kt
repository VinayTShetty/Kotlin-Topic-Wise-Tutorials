Example :-4
 
Notes :- constructor chaninging is valid in kotlin.
 
Additional Information :- 
 
Programe :-
fun main() {
    val userName=Test("Vinay T Shetty")
}

class Test  {
    constructor(username: String) :this("Vinay T Shetty","******"){
        println("Username= ${username}")
    }
    constructor(username: String,password:String):this("Vinay T Shetty","*****","vinay@gmail.com"){
        println("Username= ${username}, Password= ${password}")
    }
    constructor(username: String,password:String,email:String){
        println("Username= ${username}, Password= ${password} , Email= ${email}")
    }
}

 
Output :
 
Username= Vinay T Shetty, Password= ***** , Email= vinay@gmail.com
Username= Vinay T Shetty, Password= ******
Username= Vinay T Shetty
