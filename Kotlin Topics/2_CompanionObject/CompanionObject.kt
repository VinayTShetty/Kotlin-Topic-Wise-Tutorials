Example :-3
 
Notes :- 

There is nothing called static in Kotlin. So, in Kotlin, we use a companion object
In Kotlin, if you want to write a function or any member of the class that can be called without having the instance of the class then you can write the same as a member of a companion object inside the class
So, by declaring the companion object, you can access the members of the class by class name only(which is without explicitly creating the instance of the class).
 

Additional Information :- 
 
Programe :-
fun main() {
    Test.userName()
    println("Password = ${Test.password}")
    println("Email Id = ${Test.emailId}")
}

class Test {
    companion object credentialsDeclaration {
        fun userName() {
            println("Username= Vinay T Shetty")
        }
        val password = "*****"
        var emailId = "VinayTShetty@gmail.com"
    }
}

 
Output :
Username= Vinay T Shetty
Password = *****
Email Id = VinayTShetty@gmail.com
 
